let imgItem = document.querySelector('.imgs');
imgItem.onclick = function(){
    let mySrc = imgItem.getAttribute('src');
    if(mySrc === '../img/like_favorite_heart_3524 (1).png'){
        imgItem.setAttribute ('src','../img/heart_like_love_twitter_icon_127132.png')
    }else{
        imgItem.setAttribute ('src','../img/like_favorite_heart_3524 (1).png');
    }
}

